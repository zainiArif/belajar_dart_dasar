void main() {
  //Penjumlahan (+): Menambahkan dua nilai.
  var sum = 5 + 3; // hasilnya 8
  print(sum);

  //Pengurangan (-): Mengurangi nilai pertama dengan nilai kedua.
  var difference = 5 - 3; // hasilnya 2
  print(difference);

  // Perkalian (*): Mengalikan dua nilai.
  var product = 5 * 3; // hasilnya 15
  print(product);

  //Pembagian (/): Membagi nilai pertama dengan nilai kedua, menghasilkan nilai tipe double.
  var quotient = 5 / 2; // hasilnya 2.5
  print(quotient);

  //Pembagian Bulat (~/): Membagi nilai pertama dengan nilai kedua dan membulatkan hasilnya ke bawah menjadi integer.
  var quotient2 = 5 ~/ 2; // hasilnya 2
  print(quotient2);

  //Modulus (%): Menghasilkan sisa dari pembagian nilai pertama dengan nilai kedua.
  var remainder = 5 % 2; // hasilnya 1
  print(remainder);
  
}
